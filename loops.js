// WHILE loop: Print numbers 1 to 5
let i = 1;
while (i <= 5) {
    console.log("WHILE loop: " + i);
    i++;
}

// DO-WHILE loop: Print numbers 6 to 10
let j = 6;
do {
    console.log("DO-WHILE loop: " + j);
    j++;
} while (j <= 10);

// FOR loop: Print "Hello" 3 times
for (let k = 1; k <= 3; k++) {
    console.log("FOR loop: Hello " + k);
}
